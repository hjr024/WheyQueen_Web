   <h2 style="text-align: center; font-family: 'Bebas Neue', cursive; font-size: 40px;"> UNDERTANDING THE BASICS OF PROTEIN</h2>
The Basics of Protein
Dietary protein is an essential macronutrient, which is required by all healthy adults on a daily basis. It’s imperative that all healthy adults consume adequate amounts of protein whether they are active or not. To begin, we look at protein from two different perspectives – one from a nutritional standpoint and secondly from protein in the body. Dietary proteins are essential as they help to provide building blocks of proteins within the body. After a protein is ingested, it has the potential to perform thousands of different functions within the body from structure, function and regulation processes. These proteins in the body support vital functions and they function far beyond just muscle. There are many specialized proteins such as collagen, keratin, actin, myosin and more. As a matter of fact, there are potentially over a million different types of protein found in the body with many more yet to be discovered. Let’s take a closer look at protein from a dietary stance.    </p>
 <h2 style="text-align: center; font-family: 'Bebas Neue', cursive; font-size: 40px;"> The Building Blocks of the Body</h2>
  <p>
  Different dietary proteins are comprised of different combinations of amino acids. These amino acid profiles can impact how it’s used in the body. It all begins with the foundation of proteins, known as amino acids. Proteins are complex molecules made from twenty-plus basic building blocks, called amino acids. Some amino acids can be made in the body, while others must be obtained through the diet. The amino acids can be categorized as essential or non-essential. Essential Amino Acids (EAAs) must be consumed through our diet, because the body cannot make it on its own. There are nine EAAs, which include the following amino acids: Histidine, Isoleucine, Leucine, Lysine, Methionine, Phenylalanine, Threonine, Tryptophan, and Valine. The remaining amino acids are called non-essential amino acids, because they can be produced in the body. In all, the combination and sequence in which the amino acids arranged determine how the protein will function in the body. Therefore, it’s important to consume adequate amounts of a variety of different protein sources to help provide the body with the tools it needs to create the building blocks of the body.  </p>
     <h2 style="text-align: center; font-family: 'Bebas Neue', cursive; font-size: 40px;">Protein Quality</h2>
     <p>
When it comes to protein quality, not all protein sources are created equal. Different protein sources have different benefits, calories, and nutritional value and amino acid profiles. The quality of the protein source is determined by the amino acids content and its digestibility. Proteins can be categorized as complete or incomplete. A complete protein means that it contains all of the EAAs in sufficient quantities. Most animal-based sources are considered complete, some examples include foods such as chicken, turkey, beef, fish, pork, dairy, and eggs. There are also a few select complete plant-based proteins, which include soy, quinoa and pistachios. On the other hand, an incomplete protein means it is low or lacking in one or more EAAs. Most plant-based sources are considered incomplete. Incomplete protein include foods like beans, peas, lentils, nuts, and seeds. Interestingly, if you don’t consume dairy or animal-based proteins you can combine incomplete proteins to create a complete protein. It’s encouraged to consume a variety of dietary sources throughout the day to help meet dietary needs. Variety and balance are key.
     </p>
