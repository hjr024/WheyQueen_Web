     <p>
The Chinese Dietary Guidelines under the Chinese Nutrition Society recommend to “eat moderate amounts of fish, poultry, eggs, and lean meat.” It’s important that all healthy adults consume a variety of proteins. This is because both animal and plant-based proteins have benefit, but supply varying amounts and ratios of amino acids. If you consume a variety of proteins, then you can increase the spectrum of amino acids in the diet. When trying to get enough protein in your diet, it’s important to choose a variety of proteins. By varying the types of protein sources you take in each day, you will conversely vary the amount of amino acids consumed. Many times people get stuck eating the same two or three foods day in and day out. While we all have our favorites, changing up your routine, including the foods you eat, may be a welcomed change. Also, if the majority of your protein is coming from plant-based sources, then try to incorporate a variety of plant proteins to help ensure you’re getting all of the amino acids you need. The table below provides examples of protein-containing dietary sources.     </p>
     
     
